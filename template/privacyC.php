<h2 class="titoloP">Chi siamo?</h2>

<p class="welcome-messagee">Mondo Morbidoso è un sito web dedicato alla vendita di peluche e prodotti correlati.</p>

<h2 class="titoloP">Quali dati raccogliamo?</h2>

<p class="welcome-messagee">Raccogliamo i seguenti dati personali:</p>

<ul class="ulP">
   <li>Dati identificativi: Nome, cognome, indirizzo email, numero di telefono.</li>
   <li>Dati di pagamento: Solo se effettui acquisti.</li>
   <li>Dati di navigazione: Indirizzo IP, browser utilizzato, pagine visitate, tempo di permanenza.</li>
</ul>

<h2 class="titoloP">Come utilizziamo i tuoi dati?</h2>

<p class="welcome-messagee">I tuoi dati vengono raccolti per i seguenti scopi:</p>

<ul class="ulP"> 
    <li>Gestire gli ordini e la spedizione dei prodotti.</li>
    <li>Offrire assistenza clienti e rispondere alle richieste.</li>
    <li>Migliorare il nostro sito e la tua esperienza di navigazione.</li>
</ul>

<h2 class="titoloP">Base giuridica del trattamento</h2>

<p class="welcome-messagee">Trattiamo i tuoi dati solo nei seguenti casi:</p>

<ul class="ulP">
    <li>Hai dato il tuo consenso esplicito.</li>
    <li>È necessario per l'esecuzione di un contratto.</li>
    <li>È richiesto per obblighi legali.</li>
</ul>

<h2 class="titoloP">Condivisione dei dati</h2>

<p class="welcome-messagee">I tuoi dati non verranno venduti o ceduti a terzi, tranne nei seguenti casi:</p>

<ul class="ulP">
    <li>Fornitori di servizi di pagamento per elaborare i pagamenti.</li>
    <li>Corrieri per la spedizione degli ordini.</li>
    <li>Autorità competenti in caso di obblighi legali.</li>
</ul>

<h2 class="titoloP">Sicurezza dei dati</h2>

<p class="welcome-messagee">Proteggiamo i tuoi dati con misure di sicurezza adeguate per prevenire accessi non autorizzati.</p>

