<h1>Informativa sulla Privacy</h1>
<p>Benvenuto su Mondo Morbidoso. La tua privacy è importante per noi.<br>
Questa informativa spiega come raccogliamo,<br>
utilizziamo e proteggiamo i tuoi dati personali.</p>
<h2>Chi siamo</h2>
<p>Mondo Morbidoso è un sito web dedicato alla vendita di peluche e prodotti correlati.</p>
<h2>Quali dati raccogliamo?</h2>
<p>Raccogliamo i seguenti dati personali:</p>
<ul>
   <li>Dati identificativi: Nome, cognome, indirizzo email, numero di telefono.</li>
   <li>Dati di pagamento: Solo se effettui acquisti (trattati in modo sicuro dai nostri fornitori di pagamento).</li>
   <li>Dati di navigazione: Indirizzo IP, browser utilizzato, pagine visitate, tempo di permanenza.</li>
    </ul>
<h2>Come utilizziamo i tuoi dati?</h2>
<p>I tuoi dati vengono raccolti per i seguenti scopi:</p>
<ul>
    <li>Gestire gli ordini e la spedizione dei prodotti.</li>
    <li>Offrire assistenza clienti e rispondere alle richieste.</li>
    <li>Migliorare il nostro sito e la tua esperienza di navigazione.</li>
</ul>
<h2>Base giuridica del trattamento</h2>
<p>Trattiamo i tuoi dati solo nei seguenti casi:</p>
<ul>
    <li>Hai dato il tuo consenso esplicito (es. iscrizione alla newsletter).</li>
    <li>È necessario per l'esecuzione di un contratto (es. acquisto di un peluche).</li>
    <li>È richiesto per obblighi legali (es. fatturazione e sicurezza dei pagamenti).</li>
</ul>
<h2>Condivisione dei dati</h2>
<p>I tuoi dati non verranno venduti o ceduti a terzi, tranne nei seguenti casi:</p>
<ul>
    <li>Fornitori di servizi di pagamento (es. PayPal, Stripe) per elaborare i pagamenti.</li>
    <li>Corrieri per la spedizione degli ordini.</li>
    <li>Autorità competenti in caso di obblighi legali.</li>
</ul>
<h2>Sicurezza dei dati</h2>
<p>Proteggiamo i tuoi dati con misure di sicurezza adeguate per prevenire accessi non autorizzati.</p>
<h2>I tuoi diritti</h2>
<p>Hai diritto a:</p>
<ul>
    <li>Accedere ai tuoi dati personali.</li>
    <li>Chiedere la modifica o la cancellazione dei tuoi dati.</li>
    <li>Opporsi al trattamento per finalità di marketing.</li>
</ul>
